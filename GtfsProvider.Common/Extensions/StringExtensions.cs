        public static bool Matches(this string value, string query)
        {
            var val = value.Replace('.', ' ').Replace("  ", " ").ToLowerInvariant().RemoveDiacritics();
            var q = query.Replace('.', ' ').Replace("  ", " ").ToLowerInvariant().RemoveDiacritics();
            if (val.Contains(q))
                return true;

            var valArr = val.Split(' ');
            var qArr = q.Split(' ');

            var valIx = 0;
            foreach (var qpart in qArr)
            {
                for (; valIx < valArr.Length; valIx++)
                {
                    if (valArr[valIx].StartsWith(qpart))
                        break;
                }

                if (valIx == valArr.Length)
                    break;
            }

            return valIx < valArr.Length;
        }

